import mongoose from "mongoose";

export const usuariosModelo = mongoose.model('usuarios', new mongoose.Schema({
    nombre: {
        type: String,
        required: true
    },
    email: {
        type: String,
        unique: true,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    rol: {
        type: String,
        default: 'usuario' 
    }
}));
    